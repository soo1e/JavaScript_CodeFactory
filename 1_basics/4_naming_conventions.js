/**
 * Naming Conventions
 * 이름을 지을 때 지켜야 하는 것!
 *
 * 1) 일반적으로 영어(라틴문자)를 사용하며 문자와 숫자를 모두 사용 할 수 있다.
 * 2) 특수기호는 _와 $만 사용 가능!
 * 3) 숫자로 이름을 시작할 수 없다.
 * 4) 키워드는 변수명으로 사용 할 수 없다.
 *    var const 등 사용 할 수 없다.
 * */

let codefactory = '코드팩토리';
var $ive = '아이브';
const _yuJin = "안유진";

console.log(codefactory);
console.log($ive);
console.log(_yuJin);

